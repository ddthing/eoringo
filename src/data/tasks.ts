export const getEnabledTaskTemplatesByCategory = (
  templates: TaskTemplate[],
  category: TaskCategory,
) =>
  templates
    .filter((taskTemplate) => taskTemplate.enabledByDefault && taskTemplate.category === category)
    .sort((a, b) => a.priority - b.priority);

